			local function getGreeting(name)
				local hour = os.date("*t").hour
				local greetingsTable = {
					[1] = "  Sleep well",
					[2] = "  Good morning",
					[3] = "  Good afternoon",
					[4] = "  Good evening",
					[5] = "望 Good night",
				}
				local greetingIndex = (hour < 7 and 1)
					or (hour < 12 and 2)
					or (hour < 18 and 3)
					or (hour < 21 and 4)
					or 5
				return greetingsTable[greetingIndex] .. ", " .. name
			end

